const mongoose = require("mongoose");

const connectDB = (url) => {
  // RETURN a promise to await it before server start
  return mongoose.connect(url, {
    useNewUrlParser: true,
    useCreateIndex: true,
    useFindAndModify: false,
    useUnifiedTopology: true,
  });
};
module.exports = connectDB;
